const quizReducer = (state, action) => {
  switch (action.type) {
    case 'SELECT_QUIZ':
      return {
        ...state,
        selectedQuiz: action.payload,
        started: false,
        currentQuestion: 0,
        answers: [],
        showResult: false,
        finished: false
      };
    case 'START_QUIZ':
      return {
        ...state,
        started: true,
        currentQuestion: 0,
        answers: [],
        showResult: false,
        finished: false
      };
    case 'ANSWER_QUESTION':
      const newAnswers = [...state.answers, action.payload];
      const currentQuestions = getCurrentQuestions(state.selectedQuiz);
      const isFinished = newAnswers.length === currentQuestions.length;
      return {
        ...state,
        answers: newAnswers,
        showResult: true,
        finished: isFinished,
        currentQuestion: isFinished ? state.currentQuestion : state.currentQuestion + 1
      };
    case 'NEXT_QUESTION':
      return {
        ...state,
        showResult: false
      };
    case 'RESTART_QUIZ':
      return {
        ...state,
        started: false,
        currentQuestion: 0,
        answers: [],
        showResult: false,
        finished: false
      };
    case 'BACK_TO_MENU':
      return {
        selectedQuiz: null,
        started: false,
        currentQuestion: 0,
        answers: [],
        showResult: false,
        finished: false
      };
    default:
      return state;
  }
};

